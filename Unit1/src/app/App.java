package app;

public class App {
    public static void main(String[] args) throws Exception {

        int myint = 10;

        if (myint % 2 == 0) {
            System.out.println(myint + " is an even number");
        } else {
            System.out.println(myint + " is an odd number");
        }
        System.out.println("all finished");

    }
}